    internal GameState Simulate(MoveSet myMoveSet, MoveSet opponentMoveSet)
    {
        //Console.Error.WriteLine($"Simulating move set for me: {string.Join(", ", myMoveSet.Moves.Select(m => $"{m.SnakeId} {m.Direction}"))}");

internal sealed class PositionChecker
{
    private Game _game;
    private readonly Level _level;

    internal PositionChecker(Game game, Level level)
    {
        _game = game;
        _level = level;
    }

    internal bool IsOutOfMapBounds(Point pointToCheck)
    {
        if (pointToCheck.X < 0 || pointToCheck.X >= _game.Width || pointToCheck.Y < 0 || pointToCheck.Y >= _game.Height)
        {
            return true;
        }
        return false;
    }

    internal bool IsBlocking(Point newHeadPosition, SnakeBot snakeBot)
    {
        // Flood fill algorithm to check if the new position would block the snake in
        var visited = new HashSet<Point>();
        var queue = new Queue<Point>();

        queue.Enqueue(newHeadPosition);
        visited.Add(newHeadPosition);

        while (queue.Count > 0)
        {
            Point checkPoint = queue.Dequeue();

            var adjacentPoints = new List<Point>()
            {
                new Point(checkPoint.X + 1, checkPoint.Y),
                new Point(checkPoint.X - 1, checkPoint.Y),
                new Point(checkPoint.X, checkPoint.Y + 1),
                new Point(checkPoint.X, checkPoint.Y - 1)
            };

            foreach (var adjacentPoint in adjacentPoints)
            {
                if (adjacentPoint.X >= 0
                    && adjacentPoint.X < _game.Width
                    && adjacentPoint.Y >= 0
                    && adjacentPoint.Y < _game.Height
                    && !IsPlatform(adjacentPoint)
                    && !IsPointInAnySnake(adjacentPoint, countTails: true)
                    && !visited.Contains(adjacentPoint))
                {
                    queue.Enqueue(adjacentPoint);
                    visited.Add(adjacentPoint);
                }
            }

            if (visited.Count > snakeBot.Body.Count)
            {
                return false;
            }
        }

        if (visited.Count < snakeBot.Body.Count)
        {
            return true;
        }

        return false;
    }

    internal bool IsPlatform(Point pointToCheck)
    {
        if (IsOutOfMapBounds(pointToCheck))
        {
            return false;
        }

        if (_level.IsPlatform(pointToCheck))
        {
            return true;
        }

        return false;
    }

    internal bool IsPointInAnySnake(Point pointToCheck, bool countTails, int excludeSnakeId=-1)
    {
        foreach (var snakeBot in _game.MySnakeBots)
        {
            if (excludeSnakeId > 0 && snakeBot.Id == excludeSnakeId)
            {
                continue;
            }

            if (IsPointInGivenSnake(snakeBot.Body, pointToCheck, countTails))
            {
                return true;
            }
        }

        foreach (var snakeBot in _game.OpponentSnakeBots)
        {
            if (snakeBot.Body.Contains(pointToCheck))
            {
                if (IsPointInGivenSnake(snakeBot.Body, pointToCheck, countTails))
                {
                    return true;
                }
            }
        }

        return false;
    }

    internal bool IsPointInGivenSnake(List<Point> snakeBody, Point pointToCheck, bool countTails)
    {
        if (snakeBody.Contains(pointToCheck))
        {
            if (!countTails)
            {
                if (snakeBody[snakeBody.Count - 1] == pointToCheck)
                {
                    return false;
                }
                else
                {
                    return true;
                }
            }
            else
            {
                return true;
            }
        }

        return false;
    }

    internal bool IsStuckMove(Point newHeadPosition, SnakeBot snakeBot)
    {
        if (snakeBot.IsStuck())
        {
            if (newHeadPosition == snakeBot.GetLastMove())
            {
                return true;
            }
        }

        return false;
    }

}


internal class SnakeBot
{
    internal int Id { get; }
    internal List<Point> Body { get; set; }
    
    internal bool Remove { get; set; }

    private List<Point> previousMoves = new List<Point>();

    private Dictionary<Point, int> _attemptsAtPowerSources = new Dictionary<Point, int>();

    internal SnakeBot(int id)
    {
        Id = id;

        Body = new List<Point>();
    }

    internal void AddMove(Point move)
    {
        previousMoves.Add(move);
    }

    internal bool IsStuck()
    {
        if (previousMoves.Count < 4)
        {
            return false;
        }

        int repetitions = 0;
        Point LastMove = previousMoves.Last();

        for (int i = previousMoves.Count - 2; i >= 0; --i)
        {
            if (previousMoves[i] == previousMoves.Last())
            {
                repetitions++;
            }
            else
            {
                break;
            }
        }

        if (repetitions > 3)
        {
            return true;
        }

        return false;
    }

    internal Point GetLastMove()
    {
        if (previousMoves.Count == 0)
        {
            return new Point(-1, -1);
        }
        return previousMoves.Last();
    }

    internal void AddAttemptAtPowerSource(Point powerSource)
    {
        if (_attemptsAtPowerSources.ContainsKey(powerSource))
        {
            _attemptsAtPowerSources[powerSource]++;
        }
        else
        {
            _attemptsAtPowerSources[powerSource] = 1;
        }
    }

    internal int GetAttemptsAtPowerSource(Point powerSource)
    {
        if (_attemptsAtPowerSources.ContainsKey(powerSource))
        {
            return _attemptsAtPowerSources[powerSource];
        }
        else
        {
            return 0;
        }
    }

    internal void ClearAttemptsAtPowerSource(Point powerSource)
    {
        if (_attemptsAtPowerSources.ContainsKey(powerSource))
        {
            _attemptsAtPowerSources.Remove(powerSource);
        }
    }

    internal Dictionary<Point, int> GetAttemptsAtPowerSource()
    {
        return _attemptsAtPowerSources;
    }
}



internal sealed class SnakeState : IEquatable<SnakeState>
{ 
    internal Point Position { get; private set; }
    internal List<Point> Body { get; private set; }

    private readonly int _hashCode;

    internal SnakeState(Point position, List<Point> body)
    {
        Position = position;
        Body = body;
        _hashCode = CalculateHashCode();
    }

    private int CalculateHashCode()
    {
        HashCode hash = new HashCode();
        hash.Add(Position);
        foreach (var bodyPart in Body)
        {
            hash.Add(bodyPart);
        }

        return hash.ToHashCode();
    }

    public override int GetHashCode() => _hashCode;

    public override bool Equals(object? obj)
    {
        return Equals(obj as SnakeState);
    }

    public bool Equals(SnakeState? other)
    {
        if (other == null)
        {
            return false;
        }

        if (Position != other.Position)
        {
            return false;
        }

        if (Body.Count != other.Body.Count)
        {
            return false;
        }

        for (int i = 0; i < Body.Count; i++)
        {
            if (Body[i] != other.Body[i])
            {
                return false;
            }
        }
        
        return true;
    }
}

